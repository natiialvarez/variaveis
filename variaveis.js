// criando variaveis e guardando um string
//const é uma variavel que não pode ser mudada

const primeiroNome = "Natália";

console.log(primeiroNome)

// o let é possivel criar uma variavel que possa ficar mudando

let segundoNome = "Natália"
console.log(segundoNome)

segundoNome = "Fernanda"
console.log(segundoNome)


// string template
console.log(`Meu nome é ${primeiroNome} e meu segundo nome é ${segundoNome}`)
